	SELECT CAST('2018-04-03 16:00 -5:00' AS DATETIMEOFFSET) AS ts
	UNION 
	SELECT CAST('2018-04-03 16:00 +6:00' AS DATETIMEOFFSET)
	UNION 
	SELECT CAST('2018-04-03 16:00 +11:00' AS DATETIMEOFFSET)
	UNION 
	SELECT CAST('2018-04-03 16:00:00 +0:00' AS DATETIMEOFFSET)
)
SELECT	ts
FROM	raw
ORDER BY ts

